		system.addRingBand(star, "misc", "rings_ice0", 256f, 3, Color.white, 256f, 8225, 200f);
		system.addRingBand(star, "misc", "rings_ice0", 256f, 2, Color.white, 256f, 8300, 250f);
		system.addAsteroidBelt(star, 90, 8375, 400, 125, 310, Terrain.ASTEROID_BELT,  "Finnigan's Gift");
		system.addRingBand(star, "misc", "rings_ice0", 256f, 0, Color.white, 256f, 8375, 300f);
		system.addRingBand(star, "misc", "rings_ice0", 256f, 3, Color.white, 256f, 8525, 250f);

		SectorEntityToken pirateStation2 = system.addCustomEntity("finnigans", "Finnigan's Folly", "station_pirate_type", "pirates");
